class Solution:
    def pileBox(self, box: List[List[int]]) -> int:
        if not box:
            return 0
        box.sort()
        n = len(box)
        dp = [0] * n
        # dp[0]=box[0][2]
        for i in range(n):
            dp[i] = box[i][2]
            for j in range(i):
                if box[i][0] > box[j][0] and box[i][1] > box[j][1] and box[i][2] > box[j][2]:
                    dp[i] = max(dp[j] + box[i][2], dp[i])
        return max(dp)