class Solution:
    def minDeletionSize(self, strs: List[str]) -> int:
        R, C = len(strs), len(strs[0])
        if R==1:
            return 0
        ans = 0
        d = set()
        for c in range(C):
            for r in range(1,R):
                if strs[r][c] < strs[r - 1][c]:
                    if c == 0:
                        ans += 1
                        d.add(c)
                        break
                    f = c - 1
                    while f >= 0:
                        if f in d or strs[r][f] == strs[r - 1][f]:
                            f -= 1
                            continue
                        if strs[r][f] != strs[r - 1][f]:
                            break
                    else:
                        ans += 1
                        d.add(c)
                        break

        return ans
