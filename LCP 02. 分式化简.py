class Solution:
    def fraction(self, cont: List[int]) -> List[int]:
        def gcd(x, y):
            if y == 0:
                return x
            return gcd(y, x % y)

        def rec(li):
            if len(li) == 1:
                return [li[0], 1]
            a = li[0]
            b = rec(li[1:])
            u, d = b[::-1]
            f = gcd(a * d + u, d)
            nu,nd=(a*d+u)//f,d//f
            return [nu,nd]
        return rec(cont)
