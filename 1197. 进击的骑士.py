class Solution:
    def minKnightMoves(self, x: int, y: int) -> int:
        if (x, y) == (0, 0):
            return 0
        sr, sc = 0, 0
        dirs = [(-1, -2), (-2, -1), (-2, 1), (-1, 2), (1, 2), (2, 1), (2, -1), (1, 2)]
        t = [(sr, sc)]
        visted = {(sr, sc)}
        cnt = 0
        while 1:
            tree = []
            cnt += 1
            for r, c in t:
                for dr, dc in dirs:
                    nr, nc = r + dr, c + dc
                    if (nr, nc) not in visted:
                        visted.add((nr,nc))
                        if (nr, nc) == (x, y):
                            return cnt
                        tree.append((nr, nc))
            t=tree

