class Solution:
    def constrainedSubsetSum(self, nums: List[int], k: int) -> int:
        n=len(nums)
        f=[nums[0]]+[0]*(n-1)
        li=collections.deque([0])
        ans=nums[0]
        for i in range(1,n):
            while li and i-li[0]>k:
                li.popleft()
            f[i]=max(f[li[0]],0)+nums[i] #liä¸€å®šä¼šæœ‰å…ƒç´ çš„ï¼Œå› ä¸ºliæ˜¯å•è°ƒé€’å‡é˜Ÿåˆ—ï¼Œæ¯”æ ˆé¡¶å°çš„ä¼šç›´æ¥å…¥æ ˆï¼Œæ¯”æ ˆé¡¶å¤§çš„ä¼šå¼¹å‡ºä¸€äº›å…ƒç´ åå…¥æ ˆï¼Œæ‰€ä»¥ä¸Šä¸€è½®ä¸€å®šæœ‰ä¸€ä¸ªå…ƒç´ å…¥æ ˆï¼Œæ‰€ä»¥æ ˆä¸­ä¸€å®šæœ‰å…ƒç´ 
            ans=max(ans,f[i])
            while li and f[i]>=f[li[-1]]:
                li.pop()
            li.append(i)
        return ans


