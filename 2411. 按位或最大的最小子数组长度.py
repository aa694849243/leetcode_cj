class Solution:
    def smallestSubarrays(self, nums: List[int]) -> List[int]:
        n = len(nums)
        ans = [0] * n
        ors = []
        for i in range(len(nums) - 1, -1, -1):
            num = nums[i]
            ors.append([0, i])  # orsä¸­çš„ä½ç½®ä»å¤§åˆ°å°æ’åˆ—
            k = 0
            for j in range(len(ors)):
                ors[j][0] |= num
                if ors[j][0] == ors[k][0]:
                    ors[k][1] = ors[j][1]
                else:
                    k += 1
                    ors[k] = ors[j]
            del ors[k + 1:]
            ans[i] = ors[0][1] - i + 1
        return ans

