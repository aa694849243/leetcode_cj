import functools


class Solution:
    def numRollsToTarget(self, d: int, f: int, target: int) -> int:
        mod = 10 ** 9 + 7

        @functools.lru_cache(None)
        def dp(i, status):
            if i == 0 and status == 0:
                return 1
            elif i == 0:
                return 0
            a = 0
            for j in range(1, f+1):
                a += dp(i - 1, status - j)
            a %= mod
            return a
        return dp(d,target)