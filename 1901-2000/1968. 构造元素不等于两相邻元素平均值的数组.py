class Solution:
    def rearrangeArray(self, nums: List[int]) -> List[int]:
        nums.sort()
        p=(len(nums)+1)//2
        a = nums[:p]
        b = nums[p:]
        res=[0]*len(nums)
        res[::2]=a
        res[1::2]=b[::-1]
        return res
