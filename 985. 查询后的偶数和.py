class Solution:
    def sumEvenAfterQueries(self, nums: List[int], queries: List[List[int]]) -> List[int]:
        init = sum(num for num in nums if num % 2 == 0)
        ans = []
        for num, i in queries:
            if nums[i]%2==0:
                init -= nums[i]
            nums[i] += num
            if nums[i] % 2 == 0:
                init += nums[i]
            ans.append(init)
        return ans
