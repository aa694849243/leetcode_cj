class Solution:
    def minCostII(self, costs: List[List[int]]) -> int:
        m1, m2 = sorted(costs[0])[:2]
        l2, l1 = costs[0].index(m2), costs[0].index(m1)
        k = len(costs[0])
        dp = [(m1, l1), (m2, l2)]
        for li in costs[1:]:
            x1, x2 = float('inf'), float('inf')
            tmdp = dp[:]
            for j in range(k):
                if j == dp[0][1]:
                    tmpx = dp[1][0] + li[j]
                else:
                    tmpx = dp[0][0] + li[j]
                if tmpx < x1:
                    x2 = x1
                    x1 = tmpx
                    tmdp[1] = tmdp[0]
                    tmdp[0] = (tmpx, j)
                elif tmpx < x2:
                    x2 = tmpx
                    tmdp[1] = (tmpx, j)
            dp = tmdp
        return dp[0][0]
