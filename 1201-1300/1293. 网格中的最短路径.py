class Solution:
    def shortestPath(self, grid: List[List[int]], k: int) -> int:
        t = [(0, 0, k)]
        dirs = [(0, 1), (0, -1), (1, 0), (-1, 0)]
        R, C = len(grid), len(grid[0])
        if (R, C) == (1, 1):
            return 0
        steps = 1
        visited = {(0, 0, k)}
        while True:
            tree = []
            for r, c, res in t:
                for dr, dc in dirs:
                    nres = res
                    nr = r + dr
                    nc = c + dc
                    if 0 <= nr < R and 0 <= nc < C:
                        if grid[nr][nc] == 1:
                            nres  -= 1
                        if (nr, nc) == (R - 1, C - 1):
                            return steps
                        if nres >= 0 and (nr, nc, nres) not in visited:
                            tree.append((nr, nc, nres))
                            visited.add((nr, nc, nres))
            steps += 1
            if not tree:
                break
            t = tree
        return -1


