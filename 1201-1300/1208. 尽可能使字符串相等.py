class Solution:
    def equalSubstring(self, s: str, t: str, maxCost: int) -> int:
        l, r = 0, 0
        n = len(s)
        ans = 0
        cost = 0
        while r < n:
            while r < n and cost <= maxCost:
                cost += abs(ord(s[r]) - ord(t[r]))
                ans = max(r - l, ans)
                r += 1
            if r == n and cost <= maxCost:
                ans = max(ans, r - l)
                break
            while l < n and cost > maxCost:
                cost -= abs(ord(s[l]) - ord(t[l]))
                l += 1

        return ans
