class Solution:
    def bestLine(self, points: List[List[int]]) -> List[int]:
        if not points:
            return []
        if len(points) == 1:
            return [0]
        if len(points) == 2:
            return [0, 1]

        ma = 1
        ans = []
        for i in range(len(points) - 2):
            for j in range(i + 1, len(points) - 1):
                x1 = complex(*points[i])
                x2 = complex(*points[j])
                v1 = x2 - x1
                cnt = 2
                tmp = [i, j]
                if len(points) - j + 1 <= ma:
                    break
                for k in range(j + 1, len(points)):
                    x3 = complex(*points[k])
                    v2 = x3 - x1
                    if v1.imag * v2.real == v1.real * v2.imag:
                        cnt += 1
                if cnt > ma:
                    ans = tmp
                    ma = cnt
        return ans


