class Solution:
    def insertBits(self, N: int, M: int, i: int, j: int) -> int:
        n = str(bin(N))[2:]
        m = str(bin(M))[2:]
        if len(m) < j - i + 1:
            m = '0' * (j - i + 1-len(m)) + m
        n = n[::-1]
        m = m[::-1]
        n = n[:i] + m + n[j + 1:]
        return int(n[::-1], 2)


