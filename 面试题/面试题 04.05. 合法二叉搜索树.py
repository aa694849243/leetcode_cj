# Definition for a binary tree node.
class TreeNode:
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None


class Solution:
    def isValidBST(self, root: TreeNode) -> bool:
        self.ans = True
        self.flag = float('-inf')

        def dfs(node):
            if not node:
                return
            dfs(node.left)
            if node.val <= self.flag:
                self.ans = False
            self.flag = node.val
            dfs(node.right)
        dfs(root)
        return self.ans