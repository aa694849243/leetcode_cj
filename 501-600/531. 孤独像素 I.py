class Solution:
    def findLonelyPixel(self, picture: List[List[str]]) -> int:
        R, C = len(picture), len(picture[0])
        pcc = [*zip(*picture)]
        res = 0
        for r in range(R):
            for c in range(C):
                if picture[r][c] == 'B':
                    if collections.Counter(picture[r])['B'] == 1 and collections.Counter(pcc[c])['B'] == 1:
                        res += 1
                    break
        return res
