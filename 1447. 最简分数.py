class Solution:
    def simplifiedFractions(self, n: int) -> List[str]:
        def gcd(x, y):
            if y == 0:
                return x
            else:
                return gcd(y, x % y)

        res = set()
        for den in range(2, n + 1):
            for f in range(1, den):
                coe = gcd(den, f)
                a, b = f // coe, den // coe
                res.add(str(a)+'/'+str(b))
        return list(res)

