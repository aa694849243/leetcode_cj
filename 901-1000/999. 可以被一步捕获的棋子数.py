class Solution:
    def numRookCaptures(self, board: List[List[str]]) -> int:
        for i, row in enumerate(board):
            if 'R' in row:
                r, c = i, row.index('R')
                break
        dirs = [(1, 0), (-1, 0), (0, 1), (0, -1)]
        ans = 0
        for dr, dc in dirs:
            nr, nc = r + dr, c + dc
            while 0 <= nr < len(board) and 0 <= nc < len(board[0]) and board[nr][nc] != 'B':
                if board[nr][nc] == 'p':
                    ans += 1
                    break
                nr, nc = nr + dr, nc + dc
        return ans
