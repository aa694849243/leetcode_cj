# Definition for singly-linked list.
class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution:
    def getIntersectionNode(self, headA: ListNode, headB: ListNode) -> ListNode:
        lenth1, lenth2 = 0, 0
        HA = headA
        HB = headB
        while headA:
            headA = headA.next
            lenth1 += 1
        while headB:
            headB = headB.next
            lenth2 += 1
        if lenth1 > lenth2:
            headA, headB = HA, HB
        else:
            headA, headB = HB, HA
        lenth = abs(lenth1 - lenth2)
        while lenth:
            headA = headA.next
            lenth -= 1
        while headB != headA:
            headB = headB.next
            headA = headA.next
        return headA

