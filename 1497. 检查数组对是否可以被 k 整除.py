class Solution:
    def canArrange(self, arr: List[int], k: int) -> bool:
        arr = [*map(lambda x: x % k, arr)]
        m = collections.Counter(arr)
        for i in m:
            if i == 0 and m[i] % 2 != 0:
                return False
            elif i!=0 and m[i]!=m[k-i]:
                return False
        return True
