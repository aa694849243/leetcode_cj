class Solution:
    def expand(self, s: str) -> List[str]:
        self.res = []

        def dfs(lst, s):
            if not s:
                self.res = lst
                return
            nlst = []
            if s[0] != '{':
                if not lst:
                    dfs([s[0]],s[1:])
                else:
                    for ch in lst:
                        nlst.append(ch + s[0])
                    dfs(nlst, s[1:])
            else:
                a, b = s.index('{'), s.index('}')
                li = list(s[a + 1:b].split(','))
                if not lst:
                    dfs(li,s[b+1:])
                else:
                    for ch1 in lst:
                        for ch2 in li:
                            nlst.append(ch1 + ch2)
                    dfs(nlst, s[b + 1:])

        dfs([], s)
        return sorted(self.res)


