class Solution:
    def isRobotBounded(self, instructions: str) -> bool:
        dirs = [(0, 1), (1, 0), (0, -1), (-1, 0)]
        i = 0
        x, y = 0, 0
        for s in instructions:
            dx, dy = dirs[i]
            if s == 'G':
                x += dx
                y += dy
            elif s == 'L':
                i = (i - 1) % 4
            else:
                i = (i + 1) % 4
        if (x, y) == (0, 0) or i != 0:
            return True
        return False
