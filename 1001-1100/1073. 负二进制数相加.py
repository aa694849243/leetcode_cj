class Solution:
    def addNegabinary(self, arr1: List[int], arr2: List[int]) -> List[int]:
        a = 0
        for i, val in enumerate(arr1):
            a *= -2
            a += val
        b = 0
        for i, val in enumerate(arr2):
            b *= -2
            b += val
        num = a + b
        ans = []
        if num==0:
            return [0]
        while num != 0:
            ans.append(num & 1)
            if num % 2 == 0:  # å¶æ•°
                num //= -2
            else:
                num = num // (-2) + 1
        return ans[::-1]
